<h1>"Omega – Exact time for life"</h1>
<br>
<p>
    Omega SA is a Swiss luxury watchmaker based in Biel/Bienne, Switzerland. Founded by Louis Brandt in La Chaux-de-Fonds in 1848, the company formally operated as the La Generale Watch Co. until incorporating the name Omega in 1903, becoming Louis Brandt et Frère - Omega Watch & Co.. In 1982, the company officially changed its name to Omega SA, which is currently a subsidiary of the Swiss Swatch Group. Omega opened its museum to the public in Biel/Bienne in January 1984.
    Britain's Royal Flying Corps chose Omega watches in 1917 as its official timekeepers for its combat units, as did the U.S. Army in 1918. Omega watches were the choice of NASA and the first watch on the Moon in 1969. In addition, Omega has been the official timekeeping device of the Olympic Games since 1932. James Bond has worn it in films since 1995; other famous Omega wearers, past and present, include Buzz Aldrin, George Clooney, John F. Kennedy, Mao Zedong (Chairman Mao), Elvis Presley and Prince William.
    <br>
    The forerunner of Omega, La Generale Watch Co., was founded at La Chaux-de-Fonds,
    Switzerland in 1848 by Louis Brandt, who assembled key-wound precision pocket watches from parts supplied
    by local craftsmen. He sold his watches from Italy to Scandinavia by way of England, his chief market.
    In 1894, his two sons Louis-Paul and César developed a revolutionary in-house manufacturing and total production
    control system that allowed component parts to be interchangeable. Watches developed with these techniques were
    marketed under the Omega brand of La Generale Watch Co. By 1903 the success of the Omega brand led to La Generale
    Watch Co to spin off the Omega brand as its own company, and the Omega Watch Co was officially founded in 1903.
</p>
