<h1>Tissot is "Innovators by Tradition"</h1>
<br>
<p>Early history

    Tissot was founded in 1853 by Charles-Félicien Tissot and his son Charles-Émile Tissot in the Swiss city of Le Locle, in the Neuchâtel area of the Jura Mountains. Charles-Emile Tissot left for Russia in 1858 and succeeded in selling their savonnette pocket watches across the Russian Empire. Tissot merged with Omega in 1930 and Tissot-Omega watches from this era are sought after by collectors.

    Tissot was used for downhill skiing in Switzerland in 1938 and the Davis Cup in 1957. Tissot's first engagement as an official timekeeper was in 1938 where they timed a series of Ski races in Villars-sur-Ollon, near the company's hometown in the Jura mountains.
    Recent development

    Tissot has been a subsidiary of the Swiss Swatch Group since 1983, the largest watch producer and distributor in the world. After joining Swatch, Tissot was still based in Le Locle, Switzerland and marketed in 160 countries around the world. Tissot watches are currently classified by the Swatch Group as "mid-range market" products.

    Tissot is an official timekeeper for the world championships in cycling, motorcycling, fencing and ice hockey, and so on.
    Tissot was also a key sponsor for the Formula One teams Lotus, Renault, and Sauber. For early events, handheld stopwatches were sufficient to provide official timings. Today, Tissot works with various sporting bodies to develop systems to produce ever more accurate timings for specific events. In competitive cycling, for instance,
    sensors are placed on the bikes and track which are then linked by computers to provide track timings and performance data.  </p>

